CREATE TABLE DEM (
	ID TEXT NOT NULL PRIMARY KEY,
	URI TEXT NOT NULL
);

CREATE TABLE JOURNAL (
	ID TEXT NOT NULL PRIMARY KEY,
	DATE INTEGER NOT NULL,
	URI TEXT NOT NULL,
	NOTE TEXT NOT NULL,
	CONTROL INTEGER NOT NULL,
	OBJECT INTEGER,
	FOREIGN KEY(CONTROL) REFERENCES ENU_CONTROL(ID),
	FOREIGN KEY(OBJECT) REFERENCES ENU_OBJECT(ID)
);

CREATE TABLE PROJECT (
	NAME TEXT NOT NULL PRIMARY KEY,
	NOTE TEXT NOT NULL,
	SCALE TEXT,
	TYPE INTEGER
);

CREATE TABLE ENU_CONTROL (
	ID INTEGER NOT NULL PRIMARY KEY,
	VALUE TEXT NOT NULL
);

CREATE TABLE ENU_OBJECT (
	ID INTEGER NOT NULL PRIMARY KEY,
	VALUE TEXT NOT NULL
);

CREATE TABLE STATION (
	ID TEXT NOT NULL PRIMARY KEY,
	ID_MISSION TEXT NOT NULL,
	RINEX BLOB,
	NAME TEXT,
	FOREIGN KEY(ID_MISSION) REFERENCES MISSION(ID) ON DELETE CASCADE
);

CREATE TABLE ASSETTI (
	ID TEXT NOT NULL,
	STRIP TEXT NOT NULL,
	NAME TEXT NOT NULL,
	PX FLOAT NOT NULL,
	PY FLOAT NOT NULL,
	PZ FLOAT NOT NULL,
	OMEGA FLOAT NOT NULL,
	PHI FLOAT NOT NULL,
	KAPPA FLOAT NOT NULL,
	PRIMARY KEY (STRIP, NAME)
);

